package ProxyPattern;

public class Employee {
    private int id;
    private String name;
    private String department;

    // Getters and setters
    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setDepartment(String department) {
        this.department = department;
    }
}